package com.example.okved.model;

/**
 * Стратегия подбора кода ОКВЭД по номеру телефона.
 */
public enum MatchStrategy {

    /**
     * Код выбран по максимальному совпадению суффикса номера телефона.
     */
    BEST_SUFFIX,

    /**
     * Код выбран резервным образом на основе хэша номера телефона.
     */
    FALLBACK_HASH,

    /**
     * Данные ОКВЭД недоступны или список пуст.
     */
    NO_DATA
}